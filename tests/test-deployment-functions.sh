# Test input validation functions
validate_ip() {
    ip="$1"
    if echo "$ip" | grep -qE '^[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}$'; then
        return 0
    else
        return 1
    fi
}

validate_url() {
    url="$1"
    if echo "$url" | grep -qE '^https?://'; then
        return 0
    else
        return 1
    fi
}

